// 3Q. WAP TO CHECK NUMBER IS NEGATIVE, POSITIVE, OR NEUTRAL. ?


let VALUE = parseFloat(prompt("Give me a number  : "));
if (VALUE > 0) 
{
    console.log("The number you have entered is positive (+).");
    alert("The number you have entered is positive (+) .");
} 

else if (VALUE < 0) 
{
    console.log("The number you have entered is negative (-) .");
    alert("The number you have entered is negative (-) .");
} 

else 
{
    console.log("The number  you have entered is neutral (ZERO (0)).");
    alert("The number  you have entered is neutral (ZERO (0)).");
}

