// 2Q . WAP TO CHECK GIVEN NUMBER IS DIVISIBLE BY 7 OR NOT. 

let value = parseInt(prompt("ENTER THE DIGIT : "));


if (value % 7 == 0) 
{
    console.log(value + " This Digit will be easily divsibile by 7  ");
    alert(value + " This Digit will be easily divsibile by 7 .");
} 

else 
{
    console.log(value + " This digit will not divisible by 7.");
    alert(value + " This digit will not divisible by 7.");
}

